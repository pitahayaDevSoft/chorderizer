    _lang = None

    @classmethod
    def get_lang(cls):
        if cls._lang:
            return cls._lang

        try:
            # Try to get system language
            lang, _ = locale.getdefaultlocale()
            if lang:
                lang_code = lang[:2].lower()
                if lang_code in cls.SUPPORTED:
                    cls._lang = lang_code
                    return cls._lang
        except Exception as e:
            logging.debug(f"Language detection failed: {e}")

        cls._lang = "en"
        return cls._lang

    @classmethod
    def t(cls, key, **kwargs):
        lang = cls.get_lang()
        text = cls.STRINGS[lang].get(key, cls.STRINGS["en"].get(key, key))
        if kwargs:
            return text.format(**kwargs)
        return text
